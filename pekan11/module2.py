import math

def tambah(bil1,bil2):
    return bil1 + bil2

def kurang(bil1,bil2):
    return bil1 - bil2

def kali(bil1,bil2):
    return bil1 * bil2

def bagi(bil1,bil2):
    return bil1 / bil2

def pangkat(bil1,bil2):
    return bil1 ** bil2

def modulus(bil1,bil2):
    return bil1 % bil2

def pembagian_bulat(bil1,bil2):
    return bil1 // bil2

def sin(sudut):
    radian = math.radians(sudut)
    hasil = math.sin(radian)
    return hasil

def cos(sudut):
    radian = math.radians(sudut)
    hasil = math.cos(radian)
    return hasil

def tan(sudut):
    radian = math.radians(sudut)
    hasil = math.tan(radian)
    return hasil
